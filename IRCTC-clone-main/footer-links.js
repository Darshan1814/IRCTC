// Update footer links on page load
document.addEventListener('DOMContentLoaded', function() {
  Object.keys(footerLinks).forEach(listId => {
    const list = document.getElementById(listId);
    if (list) {
      list.innerHTML = '';
      footerLinks[listId].forEach(link => {
        const li = document.createElement('li');
        li.innerHTML = `<a href="${link.url}" target="_blank" style="color: rgba(255,255,255,0.7); text-decoration: none;">${link.text}</a>`;
        list.appendChild(li);
      });
    }
  });
});